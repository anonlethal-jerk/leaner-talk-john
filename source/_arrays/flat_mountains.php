function get_sixteenths($a_Value) {
	$t_integer = floor(floor($a_Value * 16.0 + 0.5) / 16.0);
	$t_mod = floor($a_Value * 16.0 + 0.5) % 16.0;

	if ($t_mod == 0) {
		$sixteen = $t_integer;
	} else if ($t_mod % 8 == 0) {
		$sixteen = $t_integer.' '.(floor($t_mod/8)).'/2';
	} else if ($t_mod % 4 == 0) {
		$sixteen = $t_integer.' '.(floor($t_mod/4)).'/4';
	} else if ($t_mod % 2 == 0) {
		$sixteen = $t_integer.' '.(floor($t_mod/2)).'/8';
	} else {
		$sixteen = $t_integer.' '.$t_mod.'/16';
	}
	// return (floor($a_Value * 16.0 + 0.5) / 16.0).':'.$sixteen;
	return $sixteen;
}

